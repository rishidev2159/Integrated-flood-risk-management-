-- 1. Enable PostGIS Extension
CREATE EXTENSION IF NOT EXISTS postgis;

-- 2. Create Primary Elevation Table (Baseline)
CREATE TABLE IF NOT EXISTS elevation_primary (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    system_index TEXT UNIQUE,
    latitude DOUBLE PRECISION NOT NULL,
    longitude DOUBLE PRECISION NOT NULL,
    elevation DOUBLE PRECISION NOT NULL,
    geom GEOMETRY(Point, 4326),
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 3. Create Secondary Elevation Table (Current/Satellite)
CREATE TABLE IF NOT EXISTS elevation_secondary (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    system_index TEXT UNIQUE,
    latitude DOUBLE PRECISION NOT NULL,
    longitude DOUBLE PRECISION NOT NULL,
    elevation DOUBLE PRECISION NOT NULL,
    geom GEOMETRY(Point, 4326),
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 4. Create Spatial Indices for Fast Mapping
CREATE INDEX IF NOT EXISTS idx_elevation_primary_geom ON elevation_primary USING GIST (geom);
CREATE INDEX IF NOT EXISTS idx_elevation_secondary_geom ON elevation_secondary USING GIST (geom);

-- 4.1 Auto-populate geom column from lat/lng
CREATE OR REPLACE FUNCTION populate_elevation_geom()
RETURNS TRIGGER AS $$
BEGIN
    NEW.geom := ST_SetSRID(ST_MakePoint(NEW.longitude, NEW.latitude), 4326);
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_populate_elevation_primary_geom
BEFORE INSERT OR UPDATE ON elevation_primary
FOR EACH ROW EXECUTE FUNCTION populate_elevation_geom();

CREATE TRIGGER trg_populate_elevation_secondary_geom
BEFORE INSERT OR UPDATE ON elevation_secondary
FOR EACH ROW EXECUTE FUNCTION populate_elevation_geom();

-- 5. Create Flood Risk SQL View (Choice B Analysis)
-- Logic: Classifies risk based on elevation comparison where both sources indicate vulnerability.
CREATE OR REPLACE VIEW flood_risk_view AS
SELECT 
    p.system_index,
    p.latitude,
    p.longitude,
    p.elevation as elevation_baseline,
    s.elevation as elevation_current,
    (p.elevation - s.elevation) as elevation_delta,
    CASE 
        WHEN s.elevation < 19.0 THEN 'High Risk (Red)'
        WHEN s.elevation >= 19.0 AND s.elevation <= 21.0 THEN 'Moderate Risk (Yellow)'
        ELSE 'Safe Zone (Green)'
    END as risk_status,
    CASE 
        WHEN p.elevation - s.elevation > 0.5 THEN 'Worsened'
        WHEN p.elevation - s.elevation < -0.5 THEN 'Improved'
        ELSE 'Stable'
    END as change_analysis,
    p.geom
FROM elevation_primary p
JOIN elevation_secondary s ON p.system_index = s.system_index;

-- 6. RPC Function for Aggregated Analytics
-- Returns counts for each risk status
CREATE OR REPLACE FUNCTION get_risk_summary()
RETURNS TABLE (status TEXT, count BIGINT) AS $$
BEGIN
    RETURN QUERY
    SELECT risk_status, COUNT(*)
    FROM flood_risk_view
    GROUP BY risk_status;
END;
$$ LANGUAGE plpgsql;
